def setup_for_distributed(is_master):
    """
    This function disables printing when not in master process
    """
    import builtins as __builtin__

    builtin_print = __builtin__.print

    def print(*args, **kwargs):
        force = kwargs.pop("force", False)
        if is_master or force:
            builtin_print(*args, **kwargs)

    __builtin__.print = print


def setup_torch_dist_and_backend(cuda_conf, distributed_conf) -> None:
    if torch.cuda.is_available():
        torch.backends.cudnn.deterministic = cuda_conf.cudnn_deterministic
        torch.backends.cudnn.benchmark = cuda_conf.cudnn_benchmark
        torch.backends.cuda.matmul.allow_tf32 = (
            cuda_conf.matmul_allow_tf32
            if cuda_conf.matmul_allow_tf32 is not None
            else cuda_conf.allow_tf32
        )
        torch.backends.cudnn.allow_tf32 = (
            cuda_conf.cudnn_allow_tf32
            if cuda_conf.cudnn_allow_tf32 is not None
            else cuda_conf.allow_tf32
        )

    rank = setup_distributed_backend(
        distributed_conf.backend, distributed_conf.timeout_mins
    )
    torch.distributed.barrier()

    return rank


def setup_device(accelerator):
    local_rank, distributed_rank = get_machine_local_and_dist_rank()
    if accelerator == "cuda":
        device = torch.device("cuda", local_rank)
        torch.cuda.set_device(local_rank)
    elif accelerator == "cpu":
        device = torch.device("cpu")
    else:
        raise ValueError(f"Unsupported accelerator: {accelerator}")
    return device


