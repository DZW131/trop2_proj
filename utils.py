def to_json(masks, i):
    shapes = []
    for mask in masks:
        contours, _ = cv2.findContours(
            mask.astype(np.uint8), cv2.RETR_TREE, cv2.CHAIN_APPROX_SIMPLE
        )
        if len(contours) == 0:
            continue
        if len(contours) > 1:
            areas = [cv2.contourArea(contours[i]) for i in range(len(contours))]
            ind = np.argmax(areas)
        else:
            ind = 0
        if len(contours[ind]) < 3:
            continue
        shapes.append(
            {
                "points": contours[ind].reshape(-1, 2).tolist(),
                "label": i,
                "shape_type": "polygon",
                "description": f"{i}",
                "mask": None,
                "flags": {},
                "group_id": None,
            }
        )
    return shapes


